#pragma once

namespace Aspose {
namespace Cells_FOSS {

/// Specifies filter operator type.
enum class FilterOperatorType
{
    /// Equals.
    Equal = 0,
    /// Less than.
    LessThan = 1,
    /// Less than or equal to.
    LessOrEqual = 2,
    /// Not equals.
    NotEqual = 3,
    /// Greater than or equal to.
    GreaterOrEqual = 4,
    /// Greater than.
    GreaterThan = 5,
};

}}  // namespace Aspose::Cells_FOSS
