#pragma once

namespace Aspose {
namespace Cells_FOSS {

/// Specifies operator type.
enum class OperatorType
{
    /// Between.
    Between = 0,
    /// Equals.
    Equal = 1,
    /// Greater than.
    GreaterThan = 2,
    /// Greater than or equal to.
    GreaterOrEqual = 3,
    /// Less than.
    LessThan = 4,
    /// Less than or equal to.
    LessOrEqual = 5,
    /// None.
    None = 6,
    /// Not between.
    NotBetween = 7,
    /// Not equals.
    NotEqual = 8,
};

}}  // namespace Aspose::Cells_FOSS
