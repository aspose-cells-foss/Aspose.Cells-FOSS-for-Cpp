#pragma once

namespace Aspose {
namespace Cells_FOSS {

/// Specifies cell value type.
enum class CellValueType
{
    /// Blank cell.
    Blank = 0,
    /// String value.
    String = 1,
    /// Numeric value.
    Number = 2,
    /// Boolean value.
    Boolean = 3,
    /// DateTime value.
    DateTime = 4,
    /// Formula value.
    Formula = 5,
};

}}  // namespace Aspose::Cells_FOSS
