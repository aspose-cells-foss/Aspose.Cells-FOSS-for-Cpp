#pragma once

#include <cstdint>

namespace Aspose {
namespace Cells_FOSS {

/// <summary>
/// Specifies horizontal alignment type.
/// </summary>
enum class HorizontalAlignmentType : int32_t
{
    /// <summary>
    /// General alignment.
    /// </summary>
    General = 0,
    /// <summary>
    /// Left alignment.
    /// </summary>
    Left = 1,
    /// <summary>
    /// Center alignment.
    /// </summary>
    Center = 2,
    /// <summary>
    /// Right alignment.
    /// </summary>
    Right = 3,
    /// <summary>
    /// Fill alignment.
    /// </summary>
    Fill = 4,
    /// <summary>
    /// Justify alignment.
    /// </summary>
    Justify = 5,
    /// <summary>
    /// Center continuous alignment.
    /// </summary>
    CenterContinuous = 6,
    /// <summary>
    /// Distributed alignment.
    /// </summary>
    Distributed = 7
};

} // namespace Cells_FOSS
} // namespace Aspose
