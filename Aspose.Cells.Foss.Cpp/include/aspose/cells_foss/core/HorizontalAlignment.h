#pragma once

namespace Aspose {
namespace Cells_FOSS {
namespace Core {

/// Specifies horizontal alignment.
enum class HorizontalAlignment
{
    /// Represents general horizontal alignment.
    General = 0,
    /// Represents left horizontal alignment.
    Left = 1,
    /// Represents center horizontal alignment.
    Center = 2,
    /// Represents right horizontal alignment.
    Right = 3,
    /// Represents fill horizontal alignment.
    Fill = 4,
    /// Represents justify horizontal alignment.
    Justify = 5,
    /// Represents center continuous horizontal alignment.
    CenterContinuous = 6,
    /// Represents distributed horizontal alignment.
    Distributed = 7,
};

}}}  // namespace Aspose::Cells_FOSS::Core
