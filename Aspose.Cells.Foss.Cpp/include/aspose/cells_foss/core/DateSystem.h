namespace Aspose {
namespace Cells_FOSS {
namespace Core {

/// Specifies date system.
enum class DateSystem
{
    /// Date systems are the real numbers (serial values) used to represent dates.
    /// In this system, dates are represented by the number of days since January 1, 1900.
    Windows1900 = 0,
    /// The 1904 date system, where dates are represented by the number of days since January 1, 1904.
    Mac1904 = 1,
};

}}}  // namespace Aspose::Cells_FOSS::Core
